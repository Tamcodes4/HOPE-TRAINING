package DAY_2;
public class operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 16;
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(a >> 1);
        System.out.println(a << 3);
        System.out.println(~a);
    }
}
